# Strings

first_name = "Ruben"
last_name = "Lopez Gomez"
full_name = first_name + " " + last_name

print(full_name)
print(type(full_name))  # Type of a variable

# Integers

age = 28
age += 1

print(age)
print(type(age))
print("Your age is: " + str(age))  # Type casting

# Floats

height = 250.5

print(height)
print(type(height))
print("Your height is: " + str(height) + "cm")

# Booleans

is_human = True

print(is_human)
print(type(is_human))

# Multiple assignments

name, age, attractive = "Ruben", 28, True

java = python = javascript = True

# Type casting

x = 1  # int
y = 2.0  # float
z = "3"  # str

print("X is " + str(x))

print(float(x))
print(int(y))  # 2

print(z * 3)
print(int(z) * 3)
